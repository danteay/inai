SELECT
  a.*,
  FUN_OBT_PORC_ARTICULO(sa.EVALUACION_ARTICULO_ID) AS percent
  FROM
    ARTICULOS a,
    EVALUACIONES_ARTICULOS sa,
    EVALUACIONES e
    WHERE sa.EVALUACION_ID = e.EVALUACION_ID
      AND a.ARTICULO_ID = sa.ARTICULO_ID
      AND a.ESTATUS = 1
      AND e.EVALUACION_ID = 7682
      ORDER BY a.ARTICULO_ID;
