SELECT
  a.*,
  FUN_OBT_PORC_ART_EVA(7544, a.ARTICULO_ID) AS percent
  FROM
    ARTICULOS a,
    EVALUACIONES_ARTICULOS sa,
    EVALUACIONES e
    WHERE sa.EVALUACION_ID = e.EVALUACION_ID
      AND a.ARTICULO_ID = sa.ARTICULO_ID
      AND a.ESTATUS = 1
      AND e.EVALUACION_ID = 7544
      ORDER BY a.ARTICULO_ID;
